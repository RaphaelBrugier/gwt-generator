package com.objetdirect.gwt.gen.shared.dto;

import java.io.Serializable;

import com.objetdirect.gwt.umlapi.client.helpers.UMLCanvas;

/**
 * Minimal informations about a diagram to populate the explorer view.
 * @author Raphael Brugier (raphael-dot-brugier.at.gmail'dot'com)
 */
@SuppressWarnings("serial")
public class DiagramInformations implements Serializable {
	public enum Type {
		CLASS, OBJECT, HYBRYD, SEQUENCE
	}
	
	private Long key;
	
	private String name;
	
	private Type type;
	
	private String generatedUrl;
	
	private UMLCanvas canvas;
	
	
	/** Default constructor. */
	public DiagramInformations() {}
	
	
	/**
	 * Use this constructor to create a new diagram.
	 * @param name the name of the diagram
	 * @param type the type of the diagram : CLASS or OBJECT or HYBRYD or SEQUENCE
	 */
	public DiagramInformations(String name, Type type) {
		this.name = name;
		this.type = type;
	}
	
	/**
	 * This constructor should only been used from diagramService after retrieving a diagram from the base. 
	 * @param key the key of the diagram. Autogenerated by GAE.
	 * @param name the name of the diagram
	 * @param type the type of the diagram : CLASS or OBJECT or HYBRYD or SEQUENCE
	 */
	public DiagramInformations(Long key, String name, Type type) {
		this(name, type);
		this.key = key;
	}


	/**
	 * @return the key
	 */
	public Long getKey() {
		return key;
	}


	/**
	 * @param key the key to set
	 */
	public void setKey(Long key) {
		this.key = key;
	}


	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}


	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}


	/**
	 * @return the type
	 */
	public Type getType() {
		return type;
	}


	/**
	 * @param type the type to set
	 */
	public void setType(Type type) {
		this.type = type;
	}


	/**
	 * @return the generatedUrl
	 */
	public String getGeneratedUrl() {
		return generatedUrl;
	}


	/**
	 * @param generatedUrl the generatedUrl to set
	 */
	public void setGeneratedUrl(String generatedUrl) {
		this.generatedUrl = generatedUrl;
	}


	/**
	 * @return the canvas
	 */
	public UMLCanvas getCanvas() {
		return canvas;
	}


	/**
	 * @param canvas the canvas to set
	 */
	public void setCanvas(UMLCanvas canvas) {
		this.canvas = canvas;
	}
}
